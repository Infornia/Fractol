void	zoom_out_mouse(t_data *d)
{
	d->zoom += e->zoom * 0.1;
	erase_img(img);
}

void	zoom_in_mouse(t_data *d)
{
	d->zoom -= e->zoom * 0.1;
	erase_img(img);
}
